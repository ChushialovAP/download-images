    func getData(from url: URL, completion: @escaping (Data?, URLResponse?, Error?) -> ()) {
        URLSession.shared.dataTask(with: url, completionHandler: completion).resume()
    }
    
    let imageCache = NSCache<NSString, UIImage>()
        
    func downloadImage(withURL urlString: String, forCell cell: UITableViewCell) {
        guard let cell = cell as? ImageTableViewCell else { return }
        
        cell.numberImageView.image = nil
        
        if let imageFromCache = imageCache.object(forKey: urlString as NSString) {
            cell.numberImageView.image = imageFromCache
            return
        }
        
        let url = URL(string: urlString)
        
        getData(from: url!) { data, _, error in
            guard let
                    data = data,
                    error == nil
            else { return }
            
            DispatchQueue.main.async {
                let imageToCache = UIImage(data: data)
                
                self.imageCache.setObject(imageToCache!, forKey: urlString as NSString)
                
                cell.numberImageView.image = imageToCache
            }
        }
    }

